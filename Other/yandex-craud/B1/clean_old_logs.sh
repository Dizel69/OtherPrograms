#!/bin/bash

# ---------------------------
# Скрипт для очистки старых .log файлов
# ---------------------------

# Проверяем, переданы ли два аргумента
if [ $# -ne 2 ]; then
    echo "Использование: $0 <путь_к_директории> <количество_дней>"
    echo "Пример: $0 /var/log 7"
    exit 1
fi

LOG_DIR="$1"
DAYS="$2"

# Проверяем, существует ли директория
if [ ! -d "$LOG_DIR" ]; then
    echo "Ошибка: Директория '$LOG_DIR' не существует"
    exit 1
fi

# Находим файлы .log старше N дней
FILES=$(find "$LOG_DIR" -type f -name "*.log" -mtime +"$DAYS")

# Проверяем, есть ли файлы для удаления
if [ -z "$FILES" ]; then
    echo "Нет файлов .log старше $DAYS дней в директории $LOG_DIR"
    exit 0
fi

# Выводим список файлов
echo "Найдены следующие файлы .log старше $DAYS дней:"
echo "$FILES"
echo

# Запрашиваем подтверждение
read -p "Удалить эти файлы? (y/n) " CONFIRM

if [[ "$CONFIRM" == "y" || "$CONFIRM" == "Y" ]]; then
    # Удаляем файлы
    echo "$FILES" | xargs rm -f
    echo "Файлы удалены."
else
    echo "Удаление отменено."
fi
